import os
import sys

# Вопросы пользователю
print("Программа на системные инструменты.")
answer1 = input("Вы хотите страшную картинку? (да/нет): ").strip().lower()
answer2 = input("Вы не хотите посмотреть страшную картинку? (да/нет): ").strip().lower()

# Обработка ответа на первый вопрос
if answer1 == 'да':
    print("Вы выбрали страшную картинку. Готовьтесь!")
    
    # Для Windows - вызов BSOD (требуются права администратора)
    if sys.platform == "win32":
        # Вызов команды для экстренной перезагрузки
        os.system("shutdown /r /t 0")
    else:
        # Для Linux/MacOS - команда для перезагрузки (требует sudo)
        os.system("sudo reboot")

# Обработка ответа на второй вопрос
elif answer2 == 'да':
    print("Хорошо, без страшных картинок.")

else:
    print("Некорректный ввод. Программа завершена.")

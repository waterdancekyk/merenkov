#ввод числа X
X = int(input("Введите число X: "))

# Проверка
if X < 0:
    print("Факториал не определен для отрицательных чисел")
else:
    # хранения факториала
    factorial = 1
    #вычесление факториала
    for i in range(1, X + 1):
        factorial *= i

#вывод
    print(f"Факториал числа {X} = {factorial}")

def song(chords):
    # Проверка на пустой список аккордов
    if not chords:
        print("Нет аккордов для воспроизведения. Добавляем аккорд 'заключение'.")
        chords.append(("заключение", 1))

    # Перебор аккордов и вывод их длительности
    for chord, duration in chords:
        print(f"Аккорд: {chord}, Длительность: {duration} тактов")

    # Проверка на последний аккорд
    if chords[-1][1] == 0:
        print("Добавляем аккорд 'заключение'.")
        chords.append(("заключение", 1))

# Пример использования функции
if __name__ == "__main__":
    # Пример списка аккордов
    song_chords = [
        ("C", 4),
        ("G", 2),
        ("Am", 2),
        ("F", 3),
        ("E", 0)  # Например, последний аккорд имеет длительность 0
    ]

    # Вызов функции для воспроизведения песни
    song(song_chords)

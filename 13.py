def manipulate_to_one(n):
    # Проверка, что число натуральное
    if n <= 0:
        print("Введите натуральное число!")
        return
    
    # Выводим начальное число
    print(f"Начинаем с числа {n}.")

    # Выполняем операции до тех пор, пока число не станет меньше 1.4
    while n >= 1.4:
        if n % 2 == 0:  # Если число четное
            n = n / 2
            print(f"Четное: делим на 2, получаем {n}")
        else:  # Если число нечетное
            n = n * 3 + 1
            print(f"Нечетное: умножаем на 3 и прибавляем 1, получаем {n}")

    # Округляем результат до единицы
    print("Ура, победа! Мы дошли до единицы!")

# Пример использования программы
n = int(input("Введите натуральное число: "))
manipulate_to_one(n)
